"""============ COMPARISON OPERATOR ============"""
"""
    Merupakan sebuah metode untuk membandingkan dua buah nilai yang memiliki tipe data
    berupa string, numeric, dan boolean.
"""

x = 5
y = 10

print(x == y)
print(x != y)
print(x > y)
print(x < y)
print(x >= y)
print(x <= y)

"""
Output:

False
True
False
True
False
True
"""