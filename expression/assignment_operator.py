"""============ ASSIGNMENT OPERATOR ============"""
"""
     Operator ini bertujuan untuk melakukan proses assignment atau pemberian nilai 
     pada suatu variabel dengan nilai tetap.
"""

# +=
# Jika dibuat script tanpa operator assignment, maka scriptnya akan seperti ini:
# x = 11; x = x + 5;
# Jadi script ini dapat disimpulkan menjadi dengan 11 + 5 yang ditampung langsung di variabel x
x = 11
x += 5
print(x)

# -=
# Jika dibuat script tanpa operator assignment, maka scriptnya akan seperti ini:
# x = 11; x = x - 5;
# Jadi script ini dapat disimpulkan menjadi dengan 11 - 5 yang ditampung langsung di variabel x
x = 11
x -= 5
print(x)

# *=
# Jika dibuat script tanpa operator assignment, maka scriptnya akan seperti ini:
# x = 11; x = x * 5;
# Jadi script ini dapat disimpulkan menjadi dengan 11 * 5 yang ditampung langsung di variabel x
x = 11
x *= 5
print(x)

# /=
# Jika dibuat script tanpa operator assignment, maka scriptnya akan seperti ini:
# x = 11; x = x / 5;
# Jadi script ini dapat disimpulkan menjadi dengan 11 / 5 yang ditampung langsung di variabel x
x = 11
x /= 5
print(x)

#%=
# Jika dibuat script tanpa operator assignment, maka scriptnya akan seperti ini:
# x = 11; x = x % 5;
# Jadi script ini dapat disimpulkan menjadi dengan 11 % 5 yang ditampung langsung di variabel x
x = 11
x%= 5
print(x)


